console.log("Assignment Operators Examples");

// Simple assignment (=)
let x = 10;
console.log("x =", x);

// Addition assignment (+=)
x += 5; // equivalent to x = x + 5
console.log("x += 5:", x);

// Subtraction assignment (-=)
x -= 3; // equivalent to x = x - 3
console.log("x -= 3:", x);

// Multiplication assignment (*=)
x *= 2; // equivalent to x = x * 2
console.log("x *= 2:", x);

// Division assignment (/=)
x /= 4; // equivalent to x = x / 4
console.log("x /= 4:", x);

// Modulus assignment (%=)
x %= 2; // equivalent to x = x % 2
console.log("x %= 2:", x);

// Exponentiation assignment (**=)
x = 2;
x **= 3; // equivalent to x = x ** 3
console.log("x **= 3:", x);
