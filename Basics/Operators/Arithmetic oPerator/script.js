console.log("Arithmetic Operators Examples");

// Addition
let sum = 5 + 3;
console.log("5 + 3 =", sum);

// Subtraction
let difference = 10 - 4;
console.log("10 - 4 =", difference);

// Multiplication
let product = 7 * 6;
console.log("7 * 6 =", product);

// Division
let quotient = 20 / 5;
console.log("20 / 5 =", quotient);

// Modulus
let remainder = 9 % 2;
console.log("9 % 2 =", remainder);

// Increment
let increment = 10;
increment++;
console.log("Increment 10 by 1:", increment);

// Decrement
let decrement = 10;
decrement--;
console.log("Decrement 10 by 1:", decrement);
