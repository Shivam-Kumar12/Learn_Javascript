// Compound (Combined Assignment) Operators

let a = 10;
console.log(`Initial value of a: ${a}`);

a += 5; // a = a + 5
console.log(`After a += 5: ${a}`); // Output: 15

a -= 3; // a = a - 3
console.log(`After a -= 3: ${a}`); // Output: 12

a *= 2; // a = a * 2
console.log(`After a *= 2: ${a}`); // Output: 24

a /= 4; // a = a / 4
console.log(`After a /= 4: ${a}`); // Output: 6

a %= 4; // a = a % 4
console.log(`After a %= 4: ${a}`); // Output: 2

a **= 3; // a = a ** 3
console.log(`After a **= 3: ${a}`); // Output: 8

// Increment and Decrement Operators

let b = 5;
console.log(`Initial value of b: ${b}`);

b++; // b = b + 1
console.log(`After b++: ${b}`); // Output: 6

b--; // b = b - 1
console.log(`After b--: ${b}`); // Output: 5

++b; // b = b + 1
console.log(`After ++b: ${b}`); // Output: 6

--b; // b = b - 1
console.log(`After --b: ${b}`); // Output: 5
